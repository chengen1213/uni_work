public class TreeNode{
	int value;
	TreeNode left;
	TreeNode right;
	public TreeNode(int value){
		this.value = value;
		left = null;
		right = null;
	}
	public void setValue(int value){
		this.value = value;
	}
	public int getValue(){
		return this.value;
	}
	public void setLeft(TreeNode left){
		this.left = left;
	}
	public TreeNode getLeft(){
		return this.left;
	}
	public void setRight(TreeNode right){
		this.right = right;
	}
	public TreeNode getRight(){
		return this.right;
	}

}