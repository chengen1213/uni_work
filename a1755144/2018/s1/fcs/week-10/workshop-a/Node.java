public class Node{
	private int value;
	private Node next;
	
	Node(){
		this.setValue(0);
		this.setNext(null);
	}

	Node(int value){
		this.setValue(value);
		this.setNext(null);
	}

	public int getValue(){
		return this.value;
	}

	public void setValue(int value){
		this.value = value;
	}

	public Node getNext(){
		return this.next;
	}
	public void setNext(Node next){
		this.next = next;
	}

}