public class Node<T extends Comparable> {

    private T subNode;
    private Node left;
    private Node right;

    public Node() {
        subNode = null;
        left = null;
        right = null;
    }

    public Node(T subNode) {
        this.subNode = subNode;
        left = null;
        right = null;
    }

    public void print() {
        System.out.print(((DictionaryNode)subNode).getAlphabet() + " ");
    }

    public T getValue() {
        return subNode;
    }

    public void setValue(T subNode) {
        this.subNode = subNode;
    }

    public Node getLeft() {
        return left;
    }

    public void setLeft(Node left) {
        this.left = left;
    }

    public Node getRight() {
        return right;
    }

    public void setRight(Node right) {
        this.right = right;
    }
}